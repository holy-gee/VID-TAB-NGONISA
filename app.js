const categories = Object.keys(data);

// Elements
const categoriesContainer = document.getElementById("categories");
const projectsSection = document.getElementById("projects");
const projectList = document.getElementById("project-list");
const categoryTitle = document.getElementById("category-title");
const backBtn = document.getElementById("back-btn");

// Initialize: Show categories on load
function showCategories() {
  projectsSection.classList.add("hidden");
  categoriesContainer.innerHTML = "";
  categoriesContainer.classList.remove("hidden");

  categories.forEach((cat) => {
    const card = document.createElement("div");
    card.className = "category-card";
    card.textContent = cat;
    card.onclick = () => showProjects(cat);
    categoriesContainer.appendChild(card);
  });
}

// Show projects of a category
function showProjects(category) {
  categoryTitle.textContent = category;
  projectList.innerHTML = "";
  categoriesContainer.classList.add("hidden");
  projectsSection.classList.remove("hidden");

  const projects = data[category] || [];

  projects.forEach((proj) => {
    const div = document.createElement("div");
    div.className = "project-item";
    div.innerHTML = `<strong>${proj.title}</strong><br/>${proj.description}`;
    projectList.appendChild(div);
  });
}

// Back button to categories
backBtn.onclick = () => {
  showCategories();
};

// Initial call
showCategories();